.386
.MODEL FLAT
.DATA
	STRPTR DD ?
	LEN DD 0

.CODE
	_DELDOTS PROC
		PUSH EBP
		MOV EBP, ESP 
		SUB ESP, 8

		MOV ESI, [EBP + 8]
		MOV ECX, [EBP + 12]

		MOV EDI, OFFSET STRPTR

	L1:	
		MOV BL, [ESI]
		CMP BL, '.'
		JE FALSE
		MOV [EDI], BL
		INC EDI
		INC LEN
 FALSE:	
		INC ESI
		LOOP L1

		MOV EAX, OFFSET STRPTR
		ADD ESP, 8
		POP EBP
		RET
	_DELDOTS ENDP
END



